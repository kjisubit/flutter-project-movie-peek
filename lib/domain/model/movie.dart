class Movie {
  final int? id;
  final String? overview;
  final double? popularity;
  final String? posterPath;
  final String? title;
  final double? voteAverage;
  final int? voteCount;

  const Movie({
    this.id,
    this.overview,
    this.popularity,
    this.posterPath,
    this.title,
    this.voteAverage,
    this.voteCount,
  });
}
